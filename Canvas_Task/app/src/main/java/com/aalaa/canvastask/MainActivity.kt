@Composable
fun GetWeatherApp() {
    val backgroundColor = listOf(Color(0xFF2078EE), Color(0xFF74E6FE))
    val sunColor = listOf(Color(0xFFFFC200), Color(0xFFFFE100))
    Canvas(
        modifier = Modifier
            .size(100.dp)
            .padding(16.dp)
    ) {
        val width = size.width
        val height = size.height
        val path = Path().apply {
            moveTo(width.times(.76f), height.times(.72f))
            cubicTo(
                width.times(.93f),
                height.times(.72f),
                width.times(.98f),
                height.times(.41f),
                width.times(.76f),
                height.times(.40f)
            )
            cubicTo(
                width.times(.75f),
                height.times(.21f),
                width.times(.35f),
                height.times(.21f),
                width.times(.38f),
                height.times(.50f)
            )
            cubicTo(
                width.times(.25f),
                height.times(.50f),
                width.times(.20f),
                height.times(.69f),
                width.times(.41f),
                height.times(.72f)
            )
            close()
        }
        drawRoundRect(
            brush = Brush.verticalGradient(backgroundColor),
            cornerRadius = CornerRadius(50f, 50f),
        )
        drawCircle(
            brush = Brush.verticalGradient(sunColor),
            radius = width.times(.17f),
            center = Offset(width.times(.35f), height.times(.35f))
        )
        drawPath(path = path, color = Color.White.copy(alpha = .90f))
    }
}


@Composable
fun RoundedImageWithBorder(
    image: Int, // Resource ID for the image
    borderColor: List<Color>, // List of colors for the border gradient
    modifier: Modifier = Modifier,
) {
    Box(modifier = modifier) {
        // Canvas drawing for the circular border
        Canvas(modifier = Modifier.matchParentSize()) {
            val circleRadius = size.minDimension / 2
            drawCircle(
                brush = Brush.linearGradient(colors = borderColor),
                radius = circleRadius,
                style = Stroke(width = 8.dp.toPx(), cap = StrokeCap.Round)
            )
        }

        // Image inside the circular border
        Image(
            painter = painterResource(id = image),
            contentDescription = "Profile Picture",
            modifier = Modifier
                .size(100.dp) // Adjust size as needed
                .align(Alignment.Center)
                .padding(8.dp), // Adjust padding as needed
            contentScale = ContentScale.Crop
        )
    }
}